			<h2>VORSCHAU</h2>
			<p>Die 1652 in Schweinfurt gegründete Leopoldina ist die älteste naturwissenschaftlich-medizinische Akademie der Welt. Seit 1878 residiert sie in Halle an der Saale. Zum 360. Gründungsjubiläum bezieht die Leopoldina als nunmehr Nationale Akademie der Wissenschaften ihren neuen Hauptsitz am Jägerberg in unmittelbarer Nachbarschaft zur Stiftung Moritzburg. Aus diesem Anlass haben das Kunstmuseum und das Archiv der Leopoldina eine Ausstellung zu Gelehrtenporträts erarbeitet.<br />
				Aus 1.400 historischen Mitgliederporträts der Leopoldina, die sich in Bildmatrikeln erhalten haben, werden 300 Beispiele ausgewählt, um die stilgeschichtlichen Wandlungen der Selbstrepräsentation des Gelehrtenstandes, aber auch den unmittelbaren Zusammenhang zwischen Bildauffassung und Bildtechnik sichtbar zu machen. Unter der Auswahl befinden sich so prominente Wissenschaftler wie Friedrich Hoffmann, Niels Bohr, Marie Curie, Werner Heisenberg und Albert Einstein.</p>
